<?php 
namespace Google\Service\AdSenseHost;
class AdCode extends \Google\Model
{
  public $adCode;
  public $kind;

  public function setAdCode($adCode)
  {
    $this->adCode = $adCode;
  }

  public function getAdCode()
  {
    return $this->adCode;
  }

  public function setKind($kind)
  {
    $this->kind = $kind;
  }

  public function getKind()
  {
    return $this->kind;
  }
}
