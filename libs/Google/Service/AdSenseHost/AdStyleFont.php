<?php 
namespace Google\Service\AdSenseHost;
class AdStyleFont extends \Google\Model
{
  public $family;
  public $size;

  public function setFamily($family)
  {
    $this->family = $family;
  }

  public function getFamily()
  {
    return $this->family;
  }

  public function setSize($size)
  {
    $this->size = $size;
  }

  public function getSize()
  {
    return $this->size;
  }
}
