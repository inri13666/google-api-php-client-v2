  }

  public function getSmallThumbnail()
  {
    return $this->smallThumbnail;
  }

  public function setThumbnail($thumbnail)
  {
    $this->thumbnail = $thumbnail;
  }

  public function getThumbnail()
  {
    return $this->thumbnail;
  }
