  }

  public function getLarge()
  {
    return $this->large;
  }

  public function setMedium($medium)
  {
    $this->medium = $medium;
  }

  public function getMedium()
  {
    return $this->medium;
  }

  public function setSmall($small)
  {
    $this->small = $small;
  }

  public function getSmall()
  {
    return $this->small;
  }

  public function setSmallThumbnail($smallThumbnail)
