<?php 
namespace Google\Service\Calendar;
class EventSource extends \Google\Model
{
  public $title;
  public $url;

  public function setTitle($title)
  {
    $this->title = $title;
  }

  public function getTitle()
  {
    return $this->title;
  }

  public function setUrl($url)
  {
    $this->url = $url;
  }

  public function getUrl()
  {
    return $this->url;
  }
}
