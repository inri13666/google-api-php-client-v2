<?php 
namespace Google\Service\Urlshortener;
class StringCount extends \Google\Model
{
  public $count;
  public $id;

  public function setCount($count)
  {
    $this->count = $count;
  }

  public function getCount()
  {
    return $this->count;
  }

  public function setId($id)
  {
    $this->id = $id;
  }

  public function getId()
  {
    return $this->id;
  }
}
