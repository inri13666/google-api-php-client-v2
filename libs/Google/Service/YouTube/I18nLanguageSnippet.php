<?php 
namespace Google\Service\YouTube;
class I18nLanguageSnippet extends \Google\Model
{
  public $hl;
  public $name;

  public function setHl($hl)
  {
    $this->hl = $hl;
  }

  public function getHl()
  {
    return $this->hl;
  }

  public function setName($name)
  {
    $this->name = $name;
  }

  public function getName()
  {
    return $this->name;
  }
}
